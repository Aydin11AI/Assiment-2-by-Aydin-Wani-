# Write a program in python to create a dictionary and add list as values?

my_dictionary = {}

my_dictionary['names'] = ['Alice', 'Bob', 'Charlie']
my_dictionary['ages'] = [25, 30, 28]
my_dictionary['cities'] = ['New York', 'London', 'Paris']
my_dictionary['grades'] = [85.5, 92.0, 78.5]
my_dictionary['colors'] = ['red', 'green', 'blue']

my_dictionary['subjects'] = ['Math', 'Science', 'History']

print("Dictionary with list values:", my_dictionary)

print("\nAccessing elements:")
print("First name:", my_dictionary['names'][0])
print("Second age:", my_dictionary['ages'][1])
print("First city:", my_dictionary['cities'][0])
